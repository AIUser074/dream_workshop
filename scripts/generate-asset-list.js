const fs = require('fs');
const path = require('path');

const ASSETS_DIR = path.join(__dirname, '..', 'assets', 'images');
const OUTPUT_FILE = path.join(__dirname, '..', 'js', 'asset-list.js');

const IMAGE_EXTENSIONS = ['.png', '.jpg', '.jpeg', '.gif', '.webp', '.svg'];

function findImageFiles(dir) {
    let imageFiles = [];
    const files = fs.readdirSync(dir);

    for (const file of files) {
        const filePath = path.join(dir, file);
        const stat = fs.statSync(filePath);

        if (stat.isDirectory()) {
            imageFiles = imageFiles.concat(findImageFiles(filePath));
        } else {
            const ext = path.extname(file).toLowerCase();
            if (IMAGE_EXTENSIONS.includes(ext)) {
                // 'assets/images/...' 형태의 상대 경로로 변환
                const relativePath = path.relative(path.join(__dirname, '..'), filePath)
                                         .replace(/\\/g, '/'); // Windows 경로 구분자를 '/'로 변경
                imageFiles.push(relativePath);
            }
        }
    }
    return imageFiles;
}

try {
    const allImages = findImageFiles(ASSETS_DIR);
    const fileContent = `// This file is auto-generated by scripts/generate-asset-list.js
window.ALL_IMAGE_ASSETS = ${JSON.stringify(allImages, null, 4)};
`;

    fs.writeFileSync(OUTPUT_FILE, fileContent, 'utf8');
    console.log(`✅ Successfully generated asset list with ${allImages.length} images.`);
    console.log(`   -> ${path.relative(process.cwd(), OUTPUT_FILE)}`);

} catch (error) {
    console.error('❌ Error generating asset list:', error);
    process.exit(1);
}
